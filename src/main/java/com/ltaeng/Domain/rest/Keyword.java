package com.ltaeng.Domain.rest;

public class Keyword {
    String keyword;
    int keywordTotal;
    int keywordNum;
    int keywordPage;

    public String getKeyword() {
        return keyword;
    }

    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    public int getKeywordTotal() {
        return keywordTotal;
    }

    public void setKeywordTotal(int keywordTotal) {
        this.keywordTotal = keywordTotal;
    }

    public int getKeywordNum() {
        return keywordNum;
    }

    public void setKeywordNum(int keywordNum) {
        this.keywordNum = keywordNum;
    }

    public int getKeywordPage() {
        return keywordPage;
    }

    public void setKeywordPage(int keywordPage) {
        this.keywordPage = keywordPage;
    }
}
